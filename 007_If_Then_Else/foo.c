#include <stdio.h>

int main(int argc, char *argv[])
{
	int n = 10;

	if(n >= 5)
	{
		printf("n is greater or equal than to 5\n");
	}
	else
	{
		printf("Conditional statement 1 is false\n");
	}

	if(n <= 5)
	{
		printf("n is less than or equal to 5\n");
	}
	else
	{
		printf("Conditional statement 2 is false\n");
	}

	return 0;
}
